#include "libft.h"

int	ft_atoi(char *str)
{
	int p;
	int nbr;

	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	p = 1;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			p = 0;
		str++;
	}
	nbr = 0;
	while (*str >= '0' && *str <= '9')
	{
		nbr = (nbr * 10) + (*str - '0');
		str++;
	}
	return (p ? nbr : -nbr);
}
