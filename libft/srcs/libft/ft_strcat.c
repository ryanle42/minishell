#include "libft.h"

char	*ft_strcat(const char *s1, const char *s2)
{
	int		i;
	int		j;
	char	*s3;
	char	*s4;

	s3 = (char*)s1;
	s4 = (char*)s2;
	i = 0;
	j = 0;
	while (s3[i])
		i++;
	while (s4[j])
	{
		s3[i] = s4[j];
		i++;
		j++;
	}
	s3[i] = '\0';
	return (s3);
}
