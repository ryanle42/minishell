#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;
	char	nl;

	nl = '\n';
	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &nl, 1);
}
