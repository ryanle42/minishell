#include "libft.h"

int	ft_count_wds(char const *s, char c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (s[0] != c)
		j++;
	while (s[i + 1])
	{
		if ((s[i] == c) && (s[i + 1] != c))
			j++;
		i++;
	}
	return (j);
}
