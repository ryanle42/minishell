#include "libft.h"

char	*ft_strncat(const char *s1, const char *s2, size_t n)
{
	int		i;
	int		j;
	char	*s3;
	char	*s4;

	s3 = (char*)s1;
	s4 = (char*)s2;
	i = 0;
	j = 0;
	while (s3[i])
		i++;
	while (s4[j] && n > 0)
	{
		s3[i] = s4[j];
		i++;
		j++;
		n--;
	}
	s3[i] = '\0';
	return (s3);
}
